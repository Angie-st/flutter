main() {
  // 변수 선언
  // var : 데이터에 의한 추론형 변수 선언

  var name = '장비'; // 문자형 타입(String)
  // name = '유비';
  print(name);

  var height = 170; // 정수형 타입(int)
  print(height);

  var weight = 48.8; // 실수형 타입(float, double)
  weight = 65.2;
  print(weight);

  var gender = true; // 부울 타입(boolean)
  print(gender);

  print("내 친구의 이름은 " + name + "입니다.");

  var name1 = '유비';
  var name2 = "관우";

  print("name");
  print(name);
  print(name1);
  print(name2);
}
